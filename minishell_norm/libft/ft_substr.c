#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t	i;
	size_t	size_main;
	char	*str;

	i = 0;
	size_main = ft_strlen(s);
	if (start >= size_main)
		return (str = ft_strdup(""));
	if (len > size_main - start)
		len = size_main - start;
	str = malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	while (i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
